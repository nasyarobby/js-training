// berbagai macam fungsi console

// console.log
console.log("log ke console");
// console.info
console.info("log ke console juga", "dan bisa multi argument");
// console.table
console.table({ nama: "Nasyarobby", npwp: "14344555676000" });
// console.warn
console.warn("Warning: alamat belum terisi");
// console.error
console.error("Error: fatal error");

/*
bedanya apa?
 console.log dan console.info akan dikelompokkan sebagai stdout
 console.warn dan console.error akan dikelompokkan sebagai stderr
 berguna untuk mengatur logging level
 pada browser, fungsi yg berbeda jg akan ditampilkan berbeda
 dan bisa difilter
*/

